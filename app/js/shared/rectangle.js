{
    /**
     *
     * @type {HC.Rectangle}
     */
    HC.Rectangle = class Rectangle {

        /**
         *
         * @param x
         * @param y
         * @param width
         * @param height
         */
        constructor(x, y, width, height) {
            this.x = x;
            this.y = y;
            this.width = width;
            this.height = height;
        }
    }
}